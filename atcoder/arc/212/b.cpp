template<class T>
struct edge {
    int to;
    T wt;
    edge(int to, const T& wt): to(to), wt(wt) {}
};
template<class T>
using weighted_graph = vector<vector<edge<T>>>;

template<class T>
void add_undirected_edge(weighted_graph<T>& G, int u, int v, const T& wt) {
    G[u].emplace_back(v, wt);
    G[v].emplace_back(u, wt);
}

template<class T>
void add_directed_edge(weighted_graph<T>& G, int u, int v, const T& wt) {
    G[u].emplace_back(v, wt);
}

template<class T>
vector<T> Dijkstra(const weighted_graph<T>& G, int s) {
    constexpr T INF = numeric_limits<T>::max();

    int n = G.size();
    vector<T> d(n, INF);
    d[s] = T{};
    priority_queue<pair<T, int>, vector<pair<T, int>>, greater<>> Q;
    Q.emplace(T{}, s);
    while (!Q.empty()) {
        T d0;
        int u;
        tie(d0, u) = Q.top();
        Q.pop();
        if (d0 > d[u]) continue;
        for (const auto& [v, wt]: G[u]) {
            if (d[v] > d[u] + wt) {
                d[v] = d[u] + wt;
                Q.emplace(d[v], v);
            }
        }
    }
    return d;
}

int main() {
    int n, m;
    cin >> n >> m;
    vector<int> x(m), y(m);
    vector<lint> c(m);
    rep (i, m) {
        cin >> x[i] >> y[i] >> c[i];
        x[i]--;
        y[i]--;
    }

    if (x[0] == y[0]) {
        cout << c[0] << endl;
        return 0;
    }

    weighted_graph<lint> G(2 * n);
    rep (i, n) {
        add_directed_edge(G, i + n, i, 0LL);
    }
    rep (i, m) {
        if (i != 0) {
            add_directed_edge(G, x[i], y[i] + n, c[i]);
        }
    }

    lint ans = Dijkstra(G, y[0] + n)[x[0]];
    if (ans == LLONG_MAX) {
        ans = -1;
    }
    else {
        ans += c[0];
    }
    cout << ans << endl;

    return 0;
}
